package java_37;

public class LopHoc {
    private String lop;
    private String khoa;

    public LopHoc(String lop, String khoa) {
        this.lop = lop;
        this.khoa = khoa;
    }

    public String getLop() {
        return lop;
    }

    public void setLop(String lop) {
        this.lop = lop;
    }

    public String getKhoa() {
        return khoa;
    }

    public void setKhoa(String khoa) {
        this.khoa = khoa;
    }
}
